# Arithmatic operators
print("Arithmatic operators :---")
a = 10
b = 90
sum = a + b
min = b - a
mul = a * b
div = a / b
square = a ** 2
mod = a % b
print(sum)
print(min)
print(mul)
print(div)
print(square)
print(mod)
# relational operators
print("Relational operator")
less = a < b
greater = a > b
greater_equal = a >= b
less_equal = a <= b
equal = a == b
not_equal = a != b
print(less)
print(greater)
print(greater_equal)
print(less_equal)
print(equal)
print(not_equal)
# Logical operators
print("Logical operator")
andoperator = a & b
oroperator = a | b
xoroperator = a ^ b
print(andoperator)
print(oroperator)
